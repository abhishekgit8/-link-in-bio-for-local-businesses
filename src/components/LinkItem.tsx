interface LinkItemProps {
  link: LinkType;
  buttonStyle: string;
  iconColor: string;
}

export function LinkItem({ link, buttonStyle, iconColor }: LinkItemProps) {
  const [clicked, setClicked] = useState(false);

  const handleClick = async () => {
    if (clicked) return;
    setClicked(true);
    try {
      await fetch('/api/track-click', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ linkId: link.id }),
      });
    } catch {
      // silent
    }
  };

  return (
    <a
      href={link.url}
      target="_blank"
      rel="noopener noreferrer"
      className={`flex items-center gap-3 w-full px-5 py-3.5 rounded-xl transition-all duration-200 active:scale-[0.98] ${buttonStyle}`}
      data-link-id={link.id}
      onClick={handleClick}
    >
      <span style={{ color: iconColor }}>
        {getLinkIcon(link.icon || '')}
      </span>
      <span className="flex-1 text-left font-medium text-sm">{link.label}</span>
      <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" style={{ color: iconColor, opacity: 0.5 }}>
        <path d="M7 17L17 7M17 7H7M17 7v10" />
      </svg>
    </a>
  );
}
